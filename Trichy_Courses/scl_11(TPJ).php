                                toastr.success("Course Successfully Added ",
                                    "Success");

                                $(username).val("");
                                $(email).val("");
                                $(phone).val("");
                                $(enquiry).val("");
                                $(address).val("");
                            } else {
                                toastr.error("Unable to Add", "Error")
                            }
                        }
                    })
                }
            });
